package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100;
        double b = 100.1;
        boolean c = true;
        char d = 'A'; //char은 작은 따옴표
        String e = "HelloJ ava"; //String는 큰 따옴표
        //직접 적은거가 리터럴임 a에는 100이 리터럴임
        //리터럴 자체는 변하지 않아요!
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);


    }
}
